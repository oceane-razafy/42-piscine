void	ft_ultimate_div_mod(int *a, int *b)
{
	int	a_bis;
	int	b_bis;

	if (*b != 0)
	{
		a_bis = *a;
		b_bis = *b;
		*a = a_bis / b_bis;
		*b = a_bis % b_bis;
	}
}
